<p><u>WEEKLY STANDINGS</u></p>

<p>
Each week you will be required to choose the total amount of points scored in the Monday night game. This will be used as a tiebreaker in the event one or more players are
tied after all the games for the week have been played. The person who most closely predicts the total amount of points scored in the Monday night game will be declared the
winner.
</p>

<p><u>OVERALL STANDINGS</u></p>

<ul>
    <li>1st tiebreaker - most best bet wins</li>
    <li>2nd tiebreaker - most best bet ties</li>
    <li>3rd tiebreaker - most wins</li>
</ul>